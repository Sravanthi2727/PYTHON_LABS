print("Performing Relational Operations :-")
a = 5
b = 10
print("a and b values are :", a, b)
print(">:", a>b)
print("<:", a<b)
print(">=:", a>=b)
print("<=:", a<=b)
print("==:", a==b)
print("!=:", a!=b)
print("\n")


print("Performing Bitwise Operations :-")
a = 5
b = 10
print("a and b values are :", a, b)
print("|:", a|b)
print("&:", a&b)
print("^:", a^b)
print("~:", ~a)
print(">>:", a>>b)
print("<<:", a<<b)
print("\n")



print("Performing Logical Operations :-")
