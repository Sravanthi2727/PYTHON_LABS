a = 10
b = 5

print("Performing Arithematic Operations :- \n")
print("a and b values are :", a, b)
print("+:", a+b)
print("-:", a-b)
print("*:", a*b)
print("/:", a/b)
print("//:", a//b)
print("%:", a%b)
print("**:", a**b)

print("Performing Assignment Operations :- \n")
print("a and b values are :", a, b)
a+=b
print("+=:", a)
a-=b
print("-=:", a)
a*=b
print("*=:", a)
a/=b
print("/=:", a)
a//=b
print("//=:", a)


print("Performing Relational Operations :- \n")
a = 5
b = 10
print("a and b values are :", a, b)
print(">=:", a>=b)
print("<=:", a<=b)
print("==:", a==b)
print("!=:", a!=b)


print("Performing Bitwise Operations :- \n")
a = 5
b = 10
print("a and b values are :", a, b)
print("|:", a|b)
print("&:", a&b)
print("^:", a^b)


print("Performing Binary Operations :- \n")
print("a and b values are :", a, b)
print("and:", a and b)
print("or:", a or b)

